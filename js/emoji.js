  $(".show-emoji").on("click", function() {
    $('div.emoji-menu').removeClass("hide");
    $("div.mobile_scrollable_wrap").append(showEmoji('normal'));
  });
  $(document).mouseup(function(e) {
    var child = $('div.emoji-menu'); // 设置目标区域
    if (!child.is(e.target) && child.has(e.target).length === 0) {
      child.addClass("hide");
    }
  });
  $(document).on('click', 'a.btn-emoji',function() {
    $(".btn-emoji").removeClass("active");
    var elem = $(this);
    elem.addClass("active");
    $("div.mobile_scrollable_wrap").empty();
    $("div.mobile_scrollable_wrap").append(showEmoji(elem.attr('data-type')));
  });

  function showEmoji(type) {
    var i = "";
    $.each(emoji[type], function(index, data) {
      i += "<a class='btn btn-sm emoji-item' data-emoji='" + data + "'>" + data + "</a>";
    })
    return i;
  }

  $(document).on('click', 'a.emoji-item', function() {
    var emoji = $(this).attr("data-emoji");
    var content = $(".emoji-content").val();
    content = content + emoji;
    $(".emoji-content").val(content);
    $('div.emoji-menu').addClass("hide")
  });
